    private void initComp() {
        this.setDefaultCloseOperation(EXIT_ON_CLOSE);
        this.basePanel = new JPanel(new BorderLayout(0, 0));

        // add the main manu
        mainMenu mm = new mainMenu(this, this.i18n);
        this.basePanel.add(mm, BorderLayout.NORTH);

        // add the bottom panel
        this.south = new southPanel(this.i18n);
        this.basePanel.add(south, BorderLayout.SOUTH);
        
        // add the tabedPanel
        this.tp = new JTabbedPane();
        
        //Create the splitpane
        this.sp = new JSplitPane(JSplitPane.HORIZONTAL_SPLIT, new leftMenu() , tp);
        this.sp.setOneTouchExpandable(true);
        this.sp.setDividerLocation(150);
        this.basePanel.add(sp, BorderLayout.CENTER);
                
        // add the base panel to the frame        
        this.add(this.basePanel);
        south.mekeLabelsSizes();
        
        // set event listener for capslock state
        this.setFocusable(true);
        
        //set visible
        this.setVisible(true);
    }
    

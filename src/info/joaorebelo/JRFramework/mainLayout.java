    public void getTableActor() {
        String tabName = "Actores";
        Integer tabIndex = tabExist(tabName);
        if (tabIndex > -1) {
            this.tp.setSelectedIndex(tabIndex);
        } else {
            ImageIcon icon = new ImageIcon(getClass().getResource("/resources/Extras_16_16/User.png"));
            JPanel p = new JPanel(new BorderLayout());
            ActorTable a = new ActorTable(this.i18n,p);
            p.add(a, BorderLayout.CENTER);
            this.tp.addTab("Actores", icon, p);
            new JRtabCloseButton(this.tp, this.tp.getTabCount() - 1);
            
        }
    }


    /**
     * Verify if a tab exist
     *
     * @param tabName
     * @return
     */
    public int tabExist(String tabName) {
        for (int nTab = 0; nTab < this.tp.getTabCount(); nTab++) {
            if (tabName.equals(this.tp.getTitleAt(nTab))) {
                return nTab;
            }
        }
        return -1;
    }

